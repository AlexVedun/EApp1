package org.tyaa.ea1.model;

import java.io.Serializable;

/**
 *
 * @author student
 */
public class PersonModel implements Serializable {
    private static final long serialVersionUID = 1L;
    
    private Integer id;
    
    private String firstName;
    
    private String lastName;
    
    private Integer age;

    public PersonModel() {
    }

    public PersonModel(Integer id) {
        this.id = id;
    }

    public PersonModel(Integer id, String firstName, String lastName, Integer age) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PersonModel)) {
            return false;
        }
        PersonModel other = (PersonModel) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "org.tyaa.ea1.entity.Person[ id=" + id + " ]";
    }
}
